    public void ReadTable()
    {
        DefaultTableModel modelo = (DefaultTableModel) tb_orcamento.getModel();
        PedidosDAO p = new PedidosDAO();
        modelo.setNumRows(0);
        
        for(Pedidos pe : p.Read())
        {
             modelo.addRow(new Object[]{
               pe.getId(),
               pe.getAltura(),
               pe.getLargura(),
               pe.getQtd_paredes(),
               pe.getEmail(),
               pe.getValor(),
               pe.getAmbiente(),
               pe.getPossui_porta(),
               pe.getPossui_janela(),
               pe.getQtd_porta(),
               pe.getQtd_janela(),
               pe.getAltura_porta(),
               pe.getLargura_porta(),
               pe.getAltura_janela(),
               pe.getLargura_janela(),
               pe.getMao_obra()
           });
        }
    }

